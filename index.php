           <p>
               Doświadczenie zdobyte podczas pracy na wysokościach w charakterze pracownika alpinistycznego pozwoliło na wykonywanie tego typu usług samodzielnie w latach 2003-2006. W roku 2006 jako laureat konkursu na najlepszy biznes plan Michał Dawidowicz zakłada firmę Alpin-Tech w ramach Akademickich Inkubatorów Przedsiębiorczości przy Uniwersytecie Gdańskim. Wysoka jakość usług oraz indywidualne podejście do klienta owocują prężnym rozwojem firmy oraz zajęciem miejsca w pierwszej dziesiątce najlepszych firm działających w ramach AIP w Polsce w roku 2007. <br><br>Intensywnie rozwijamy zaplecze techniczne inwestując w nowe technologie, które podnoszą jakość naszych usług. Nieustannie doskonalimy umiejętności by sprostać nawet najtrudniejszym zadaniom które Państwo przed nami stawiacie. <br> <br>Zachęcam do skorzystania z naszych usług i dołączenia do grona zadowolonych klientów. <br><br> Pozdrawiam i zapraszam do współpracy.
